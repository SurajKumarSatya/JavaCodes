package inheritanceByObject;

public class Components  //parent
{
	public void run()
	{
		System.out.println("Animal will run");
	}
	
	public void bark()
	{
		System.out.println("Animal will bark");
	}
	
	public void eat()
	{
		System.out.println("Animal will eat");
	}
	
	public void sleep()
	{
		System.out.println("Animal will sleep");
	}
	
	public void play()
	{
		System.out.println("Animal will play");
	}
	
	public void fly()
	{
		System.out.println("Bird will fly");
	}
	
	protected void chirp()
	{
		System.out.println("Bird will chirp");
	}
	
	public void hunt()
	{
		System.out.println("Bird will hunt");
	}

}
